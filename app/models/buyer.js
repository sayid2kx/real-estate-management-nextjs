import mongoose, { Schema, models } from "mongoose";

const buyerSchema = new Schema(
  {
    fullname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Buyer = models.Buyer || mongoose.model("Buyer", buyerSchema);
export default Buyer;
